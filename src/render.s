# ------------------------------------------------------------
# begin_frame
# Limpa o frame atual de desenho.
#
# Entrada: draw_frame
# Saida: frame atual limpo
# Modifica: t0, t1, a0, a1, a7
# ------------------------------------------------------------

begin_frame:
    la t0, draw_frame
    lw t1, 0(t0)

    li a0, COLOR_BLACK
    mv a1, t1
    li a7, 148
    ecall

    ret

# ------------------------------------------------------------
# end_frame
# Mostra o frame recem desenhado e alterna draw_frame.
#
# Entrada: draw_frame
# Saida: frame exibido e draw_frame alternado
# Modifica: t0, t1
# ------------------------------------------------------------

end_frame:
    la t0, draw_frame
    lw t1, 0(t0)

    li t0, VGAFRAMESELECT
    sw t1, 0(t0)

    xori t1, t1, 1

    la t0, draw_frame
    sw t1, 0(t0)

    ret

# ------------------------------------------------------------
# get_draw_base_address
# Retorna em a0 o endereco base do frame atual de desenho.
#
# Entrada: draw_frame
# Saida: a0 = VGAADDRESSINI0 ou VGAADDRESSINI1
# Modifica: t0, t1
# ------------------------------------------------------------

get_draw_base_address:
    la t0, draw_frame
    lw t1, 0(t0)

    li a0, VGAADDRESSINI0
    beqz t1, end_get_draw_base_address

    li a0, VGAADDRESSINI1

end_get_draw_base_address:
    ret

update_animation_frame:
    la t0, animation_tick
    lw t1, 0(t0)
    addi t1, t1, 1

    li t2, ANIMATION_FRAME_DELAY
    blt t1, t2, store_animation_tick

    sw zero, 0(t0)

    la t0, animation_frame
    lw t1, 0(t0)
    xori t1, t1, 1
    sw t1, 0(t0)
    ret

store_animation_tick:
    sw t1, 0(t0)
    ret

draw_background:
    addi sp, sp, -8
    sw ra, 0(sp)

    call get_draw_base_address
    sw a0, 4(sp)

    li a0, 0
    li a1, 18
    li a2, SCREEN_WIDTH
    li a3, 2
    li a4, COLOR_SCENE_BORDER
    lw a5, 4(sp)
    call draw_rect

    li a0, 0
    li a1, 214
    li a2, SCREEN_WIDTH
    li a3, 2
    li a4, COLOR_SCENE_BORDER
    lw a5, 4(sp)
    call draw_rect

    la t0, current_level
    lw t1, 0(t0)

    li t2, LEVEL_TOWN
    beq t1, t2, draw_town_background

    li t2, LEVEL_SEWER
    beq t1, t2, draw_sewer_background

    li t2, LEVEL_LABORATORY
    beq t1, t2, draw_laboratory_background

    j end_draw_background

