# ------------------------------------------------------------
# draw_bullets
# Desenha todos os tiros ativos como quadrados 3x3.
#
# Entrada:
#   bullet_x
#   bullet_y
#   bullet_active
#   draw_frame
#
# Saida:
#   pixels dos tiros escritos no frame atual
#
# Modifica:
#   t0-t6, a0-a4
# ------------------------------------------------------------

draw_bullets:
    # Sprite hook: substituir corpo por draw_bullet_sprite.
    addi sp, sp, -4
    sw ra, 0(sp)

    # Pega endereco base do frame atual de desenho
    call get_draw_base_address
    mv t2, a0                  # t2 = base do framebuffer

    li t1, 0                   # indice do tiro atual

draw_bullets_loop:
    li t3, MAX_BULLETS
    beq t1, t3, end_draw_bullets

    # offset = indice * 4
    slli t4, t1, 2

    # Verifica bullet_active[indice]
    la t0, bullet_active
    add t5, t0, t4
    lw t6, 0(t5)

    # Se nao esta ativo, pula
    beqz t6, next_draw_bullet

    # Carrega bullet_x[indice] em a0
    la t0, bullet_x
    add t5, t0, t4
    lw a0, 0(t5)

    # Carrega bullet_y[indice] em a1
    la t0, bullet_y
    add t5, t0, t4
    lw a1, 0(t5)

    # Protecao: x precisa estar entre 0 e 317
    blt a0, zero, next_draw_bullet
    li t6, BULLET_SIZE
    add t0, a0, t6
    li t6, SCREEN_WIDTH
    bgt t0, t6, next_draw_bullet

    # Protecao: y precisa estar entre 0 e 237
    blt a1, zero, next_draw_bullet
    li t6, BULLET_SIZE
    add t0, a1, t6
    li t6, SCREEN_HEIGHT
    bgt t0, t6, next_draw_bullet

    # Guarda x e y do tiro
    mv a2, a0                  # a2 = x
    mv a3, a1                  # a3 = y

    # Linha local do quadrado 3x3
    li a4, 0

bullet_row_loop:
    # Coluna local do quadrado 3x3
    li t5, 0

    # y_real = y + linha
    add t6, a3, a4

    # offset = y_real * 320 + x
    # 320 = 256 + 64
    slli a0, t6, 8             # y * 256
    slli a1, t6, 6             # y * 64
    add a0, a0, a1             # y * 320
    add a0, a0, a2             # + x
    add a0, a0, t2             # + base framebuffer

bullet_col_loop:
    # endereco do pixel atual
    add a1, a0, t5

    # cor do tiro: amarelo
    li t6, 0xE7
    sb t6, 0(a1)

    addi t5, t5, 1
    li t6, 3
    blt t5, t6, bullet_col_loop

    addi a4, a4, 1
    li t6, 3
    blt a4, t6, bullet_row_loop

next_draw_bullet:
    addi t1, t1, 1
    j draw_bullets_loop

end_draw_bullets:
    lw ra, 0(sp)
    addi sp, sp, 4

    ret

