    beqz a5, draw_selected_enemy_sprite
    la a2, sprite_enemy_spitter_1

draw_selected_enemy_sprite:
    lw a0, 12(sp)
    lw a1, 16(sp)
    li a3, ENEMY_SIZE
    li a4, ENEMY_SIZE
    call draw_sprite_8bpp_fast

    lw t1, 8(sp)
    j next_draw_enemy

draw_enemy_fallback_rect:
    lw a0, 12(sp)
    lw a1, 16(sp)
    li a2, ENEMY_SIZE
    li a3, ENEMY_SIZE
    li a4, PAL_ENEMY_FALLBACK
    lw a5, 4(sp)
    call draw_rect

    lw t1, 8(sp)

next_draw_enemy:
    addi t1, t1, 1
    j draw_enemies_loop

end_draw_enemies:
    lw ra, 0(sp)
    addi sp, sp, 20

    ret

# ------------------------------------------------------------
# draw_enemy_bullets
# Desenha projeteis inimigos ativos como quadrados 3x3.
#
# Entrada:
#   enemy_bullet_x
#   enemy_bullet_y
#   enemy_bullet_active
#
# Saida:
#   pixels escritos no frame atual
#
# Modifica: t0-t6, a0-a4
# ------------------------------------------------------------

draw_enemy_bullets:
    # Sprite hook: substituir corpo por draw_enemy_bullet_sprite.
    addi sp, sp, -4
    sw ra, 0(sp)

    call get_draw_base_address
    mv t2, a0

    li t1, 0

draw_enemy_bullets_loop:
    li t3, MAX_ENEMY_BULLETS
    beq t1, t3, end_draw_enemy_bullets

    slli t4, t1, 2

    # verifica se ativo
    la t0, enemy_bullet_active
    add t5, t0, t4
    lw t6, 0(t5)
    beqz t6, next_draw_enemy_bullet

    # a0 = x
    la t0, enemy_bullet_x
    add t5, t0, t4
    lw a0, 0(t5)

    # a1 = y
    la t0, enemy_bullet_y
    add t5, t0, t4
    lw a1, 0(t5)

    # segurança x
    blt a0, zero, next_draw_enemy_bullet

    # segurança y
    blt a1, zero, next_draw_enemy_bullet

    la t0, enemy_bullet_size
    add t5, t0, t4
    lw t6, 0(t5)
    bgtz t6, enemy_draw_size_ok
    li t6, ENEMY_BULLET_SIZE

enemy_draw_size_ok:
    mv a5, t6

    add t0, a0, a5
    li t6, SCREEN_WIDTH
    bgt t0, t6, next_draw_enemy_bullet

    add t0, a1, a5
    li t6, SCREEN_HEIGHT
    bgt t0, t6, next_draw_enemy_bullet

    # guarda x/y
    mv a2, a0
    mv a3, a1

    li a4, 0

enemy_bullet_row_loop:
    li t5, 0

    # y real
    add t6, a3, a4

    # offset = y * 320 + x
    slli a0, t6, 8
    slli a1, t6, 6
    add a0, a0, a1
    add a0, a0, a2
    add a0, a0, t2

enemy_bullet_col_loop:
    add a1, a0, t5

    # cor do projetil inimigo
    li t6, 0xF0
    sb t6, 0(a1)

    addi t5, t5, 1
    blt t5, a5, enemy_bullet_col_loop

    addi a4, a4, 1
    blt a4, a5, enemy_bullet_row_loop

next_draw_enemy_bullet:
    addi t1, t1, 1
    j draw_enemy_bullets_loop

end_draw_enemy_bullets:
    lw ra, 0(sp)
    addi sp, sp, 4

    ret

