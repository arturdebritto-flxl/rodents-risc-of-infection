    mv t1, a1
    mv t2, a2
    mv t3, a3
    li t4, 0

draw_rect_row_loop:
    beq t4, t3, end_draw_rect

    add t6, t1, t4
    slli a6, t6, 8
    slli a7, t6, 6
    add a6, a6, a7
    add a6, a6, t0
    add a6, a6, a5

    li t5, 0

draw_rect_col_loop:
    beq t5, t2, next_draw_rect_row
    add a7, a6, t5
    sb a4, 0(a7)
    addi t5, t5, 1
    j draw_rect_col_loop

next_draw_rect_row:
    addi t4, t4, 1
    j draw_rect_row_loop

end_draw_rect:
    ret

draw_sprite_16:
    mv t0, a0
    mv a0, a1
    mv a1, a2
    mv a2, t0
    li a3, 16
    li a4, 16
    j draw_sprite_8bpp_fast

draw_sprite_32:
    mv t0, a0
    mv a0, a1
    mv a1, a2
    mv a2, t0
    li a3, 32
    li a4, 32
    j draw_sprite_8bpp_fast

draw_sprite_square:
    mv t0, a0
    mv a0, a1
    mv a1, a2
    mv a2, t0
    mv a3, a4
    j draw_sprite_8bpp_fast

# ------------------------------------------------------------
# draw_sprite_8bpp_fast
# Desenha sprite 8bpp no frame atual de desenho.
#
# Entrada:
#   a0 = x
#   a1 = y
#   a2 = endereco do sprite
#   a3 = largura
#   a4 = altura
#
# Regras:
#   pixel 0 e transparente
#   get_draw_base_address e chamado uma vez por sprite
#   caminho rapido sem bounds check por pixel quando cabe na tela
# ------------------------------------------------------------

draw_sprite_8bpp_fast:
    addi sp, sp, -28
    sw ra, 0(sp)
    sw a0, 4(sp)
    sw a1, 8(sp)
    sw a2, 12(sp)
    sw a3, 16(sp)
    sw a4, 20(sp)

    call get_draw_base_address
    sw a0, 24(sp)

    lw t0, 4(sp)
    lw t1, 8(sp)
    lw t2, 16(sp)
    lw t3, 20(sp)

    blt t0, zero, draw_sprite_safe_path
    blt t1, zero, draw_sprite_safe_path

    add t4, t0, t2
    li t5, SCREEN_WIDTH
    bgt t4, t5, draw_sprite_safe_path

    add t4, t1, t3
    li t5, SCREEN_HEIGHT
    bgt t4, t5, draw_sprite_safe_path

    lw a0, 24(sp)
    lw t2, 12(sp)
    li t4, 0

draw_sprite_fast_row_loop:
    lw t3, 20(sp)
    beq t4, t3, end_draw_sprite_8bpp_fast

    lw t1, 8(sp)
    add t6, t1, t4
    slli a5, t6, 8
    slli a6, t6, 6
    add a5, a5, a6
    lw t0, 4(sp)
    add a5, a5, t0
    add a5, a5, a0

    li t5, 0

draw_sprite_fast_col_loop:
    lw t3, 16(sp)
    beq t5, t3, next_draw_sprite_fast_row

    lbu a7, 0(t2)
    addi t2, t2, 1
    beqz a7, next_draw_sprite_fast_col

    add a6, a5, t5
    sb a7, 0(a6)

next_draw_sprite_fast_col:
    addi t5, t5, 1
    j draw_sprite_fast_col_loop

next_draw_sprite_fast_row:
    addi t4, t4, 1
    j draw_sprite_fast_row_loop

draw_sprite_safe_path:
    lw a0, 24(sp)
    lw t0, 4(sp)
    lw t1, 8(sp)
    lw t2, 12(sp)
    lw t3, 16(sp)
    lw t4, 20(sp)
    li t5, 0

draw_sprite_safe_row_loop:
    beq t5, t4, end_draw_sprite_8bpp_fast

    add a5, t1, t5
    li a6, 0

draw_sprite_safe_col_loop:
    beq a6, t3, next_draw_sprite_safe_row

    lbu a7, 0(t2)
    addi t2, t2, 1
    beqz a7, next_draw_sprite_safe_col

    add t6, t0, a6
    blt t6, zero, next_draw_sprite_safe_col
    li a1, SCREEN_WIDTH
    bge t6, a1, next_draw_sprite_safe_col

    blt a5, zero, next_draw_sprite_safe_col
    li a1, SCREEN_HEIGHT
    bge a5, a1, next_draw_sprite_safe_col

    slli a1, a5, 8
    slli a2, a5, 6
    add a1, a1, a2
    add a1, a1, t6
    add a1, a1, a0
    sb a7, 0(a1)

next_draw_sprite_safe_col:
    addi a6, a6, 1
    j draw_sprite_safe_col_loop

next_draw_sprite_safe_row:
    addi t5, t5, 1
    j draw_sprite_safe_row_loop

end_draw_sprite_8bpp_fast:
    lw ra, 0(sp)
    addi sp, sp, 28
    ret

draw_small_digit:
    blt a0, zero, end_draw_small_digit
    li t0, 9
    bgt a0, t0, end_draw_small_digit

    la t0, digit_mask_table
    li t1, 5
    mul t2, a0, t1
    add t0, t0, t2

    li t1, 0

small_digit_row_loop:
    lb t2, 0(t0)
    li t3, 0

small_digit_col_loop:
    li t4, 2
    sub t4, t4, t3
    srl t5, t2, t4
    andi t5, t5, 1
    beqz t5, next_small_digit_col

    add t5, a2, t1
    slli a5, t5, 8
    slli a6, t5, 6
    add a5, a5, a6
    add a5, a5, a1
    add a5, a5, t3
    add a5, a5, a4
    sb a3, 0(a5)

next_small_digit_col:
    addi t3, t3, 1
    li t4, 3
    blt t3, t4, small_digit_col_loop

    addi t0, t0, 1
    addi t1, t1, 1
    li t4, 5
    blt t1, t4, small_digit_row_loop

end_draw_small_digit:
    ret

draw_small_char:
    li t0, ':'
    beq a0, t0, small_char_colon

    li t0, '/'
    beq a0, t0, small_char_slash

    li t0, 'A'
    blt a0, t0, end_draw_small_char
    li t0, 'Z'
    bgt a0, t0, end_draw_small_char

    addi t0, a0, -65
    li t1, 5
    mul t2, t0, t1
    la t0, letter_mask_table
    add t0, t0, t2
    j small_char_draw_mask

small_char_colon:
    addi t0, a2, 1
    slli t1, t0, 8
    slli t2, t0, 6
    add t1, t1, t2
    add t1, t1, a1
    add t1, t1, a4
    sb a3, 0(t1)

    addi t0, a2, 3
    slli t1, t0, 8
    slli t2, t0, 6
    add t1, t1, t2
    add t1, t1, a1
    add t1, t1, a4
    sb a3, 0(t1)
    ret

small_char_slash:
    addi t0, a2, 4
    slli t1, t0, 8
    slli t2, t0, 6
    add t1, t1, t2
    add t1, t1, a1
    add t1, t1, a4
    sb a3, 0(t1)

    addi t0, a2, 3
    slli t1, t0, 8
    slli t2, t0, 6
    add t1, t1, t2
    add t1, t1, a1
    addi t1, t1, 1
    add t1, t1, a4
    sb a3, 0(t1)

    addi t0, a2, 2
    slli t1, t0, 8
    slli t2, t0, 6
    add t1, t1, t2
    add t1, t1, a1
    addi t1, t1, 1
    add t1, t1, a4
    sb a3, 0(t1)

    addi t0, a2, 1
    slli t1, t0, 8
    slli t2, t0, 6
    add t1, t1, t2
    add t1, t1, a1
    addi t1, t1, 2
    add t1, t1, a4
    sb a3, 0(t1)

    add t1, a1, a4
    addi t1, t1, 2
    sb a3, 0(t1)
    ret

small_char_draw_mask:
    li t1, 0

small_char_row_loop:
    lb t2, 0(t0)
    li t3, 0

small_char_col_loop:
    li t4, 2
    sub t4, t4, t3
    srl t5, t2, t4
    andi t5, t5, 1
    beqz t5, next_small_char_col

    add t5, a2, t1
    slli a5, t5, 8
    slli a6, t5, 6
    add a5, a5, a6
    add a5, a5, a1
    add a5, a5, t3
    add a5, a5, a4
    sb a3, 0(a5)

next_small_char_col:
    addi t3, t3, 1
    li t4, 3
    blt t3, t4, small_char_col_loop

    addi t0, t0, 1
    addi t1, t1, 1
    li t4, 5
    blt t1, t4, small_char_row_loop

end_draw_small_char:
    ret

draw_small_text:
    addi sp, sp, -24
    sw ra, 0(sp)
    sw a0, 4(sp)
    sw a1, 8(sp)
    sw a2, 12(sp)
    sw a3, 16(sp)
    sw a4, 20(sp)

small_text_loop:
    lw t0, 4(sp)
    lb a0, 0(t0)
    beqz a0, end_draw_small_text

    lw a1, 8(sp)
    lw a2, 12(sp)
    lw a3, 16(sp)
    lw a4, 20(sp)
    call draw_small_char

    lw t0, 4(sp)
    addi t0, t0, 1
    sw t0, 4(sp)

    lw t0, 8(sp)
    addi t0, t0, 4
    sw t0, 8(sp)

    j small_text_loop

end_draw_small_text:
    lw ra, 0(sp)
    addi sp, sp, 24
    ret

draw_small_number:
    addi sp, sp, -28
    sw ra, 0(sp)
    sw a0, 4(sp)
    sw a1, 8(sp)
    sw a2, 12(sp)
    sw a3, 16(sp)
    sw a4, 20(sp)
    sw zero, 24(sp)

    lw t0, 4(sp)
    blt t0, zero, clamp_small_number_zero

    li t1, 9999
    ble t0, t1, small_number_in_range
    sw t1, 4(sp)
    j small_number_in_range

clamp_small_number_zero:
    sw zero, 4(sp)

small_number_in_range:
    lw t0, 4(sp)
    li t1, 1000
    div a0, t0, t1
    rem t0, t0, t1
    sw t0, 4(sp)
    bnez a0, draw_number_thousands
    lw t2, 24(sp)
    beqz t2, skip_number_thousands

draw_number_thousands:
    lw a1, 8(sp)
    lw a2, 12(sp)
    lw a3, 16(sp)
    lw a4, 20(sp)
    call draw_small_digit
    lw t0, 8(sp)
    addi t0, t0, 4
    sw t0, 8(sp)
    li t0, 1
    sw t0, 24(sp)

skip_number_thousands:
    lw t0, 4(sp)
    li t1, 100
    div a0, t0, t1
    rem t0, t0, t1
    sw t0, 4(sp)
    bnez a0, draw_number_hundreds
    lw t2, 24(sp)
    beqz t2, skip_number_hundreds

draw_number_hundreds:
    lw a1, 8(sp)
    lw a2, 12(sp)
    lw a3, 16(sp)
    lw a4, 20(sp)
    call draw_small_digit
    lw t0, 8(sp)
    addi t0, t0, 4
    sw t0, 8(sp)
    li t0, 1
    sw t0, 24(sp)

skip_number_hundreds:
    lw t0, 4(sp)
    li t1, 10
    div a0, t0, t1
    rem t0, t0, t1
    sw t0, 4(sp)
    bnez a0, draw_number_tens
    lw t2, 24(sp)
    beqz t2, skip_number_tens

draw_number_tens:
    lw a1, 8(sp)
    lw a2, 12(sp)
    lw a3, 16(sp)
    lw a4, 20(sp)
    call draw_small_digit
    lw t0, 8(sp)
    addi t0, t0, 4
    sw t0, 8(sp)

skip_number_tens:
    lw a0, 4(sp)
    lw a1, 8(sp)
    lw a2, 12(sp)
    lw a3, 16(sp)
    lw a4, 20(sp)
    call draw_small_digit

    lw ra, 0(sp)
    addi sp, sp, 28
    ret

# ------------------------------------------------------------
# draw_player_square
# Desenha o jogador como sprite 16x16 ou fallback simples.
#
# Entrada:
#   player_x
#   player_y
#   draw_frame
#
# Saida:
#   pixels escritos no frame atual de desenho
#
# Modifica:
#   t0-t6, a0-a4
# ------------------------------------------------------------

draw_player_square:
    addi sp, sp, -4
    sw ra, 0(sp)

    li t0, USE_SPRITE_PLAYER
    beqz t0, draw_player_fallback_rect

    la t0, player_direction
    lw t1, 0(t0)

    la t0, animation_frame
    lw t6, 0(t0)
    andi t6, t6, 1

    li t2, DIR_UP
    beq t1, t2, select_player_up_sprite

    li t2, DIR_LEFT
    beq t1, t2, select_player_left_sprite

    li t2, DIR_RIGHT
    beq t1, t2, select_player_right_sprite

select_player_down_sprite:
    la a2, sprite_player_down_0
    beqz t6, draw_selected_player_sprite
    la a2, sprite_player_down_1
    j draw_selected_player_sprite

select_player_up_sprite:
    la a2, sprite_player_up_0
    beqz t6, draw_selected_player_sprite
    la a2, sprite_player_up_1
    j draw_selected_player_sprite

select_player_left_sprite:
    la a2, sprite_player_left_0
    beqz t6, draw_selected_player_sprite
    la a2, sprite_player_left_1
    j draw_selected_player_sprite

select_player_right_sprite:
    la a2, sprite_player_right_0
    beqz t6, draw_selected_player_sprite
    la a2, sprite_player_right_1

draw_selected_player_sprite:
    la t0, player_x
    lw a0, 0(t0)

    la t0, player_y
    lw a1, 0(t0)

    li a3, PLAYER_SIZE
    li a4, PLAYER_SIZE
    call draw_sprite_8bpp_fast
    j end_draw_player_square

draw_player_fallback_rect:
    call get_draw_base_address
    mv a5, a0

    la t0, player_x
    lw a0, 0(t0)

    la t0, player_y
    lw a1, 0(t0)

    li a2, PLAYER_SIZE
    li a3, PLAYER_SIZE
    li a4, COLOR_PLAYER_FALLBACK
    call draw_rect

end_draw_player_square:
    lw ra, 0(sp)
    addi sp, sp, 4

    ret

# ------------------------------------------------------------
# draw_bullets
# Desenha todos os tiros ativos como quadrados 3x3.
#
# Entrada:
#   bullet_x
#   bullet_y
#   bullet_active
#   draw_frame
#
# Saida:
#   pixels dos tiros escritos no frame atual
#
# Modifica:
#   t0-t6, a0-a4
# ------------------------------------------------------------

draw_bullets:
    addi sp, sp, -16
    sw ra, 0(sp)

    li t1, 0                   # indice do tiro atual

draw_bullets_loop:
    li t3, MAX_BULLETS
    beq t1, t3, end_draw_bullets

    # offset = indice * 4
    slli t4, t1, 2

    # Verifica bullet_active[indice]
    la t0, bullet_active
    add t5, t0, t4
    lw t6, 0(t5)

    # Se nao esta ativo, pula
    beqz t6, next_draw_bullet

    # Carrega bullet_x[indice] em a0
    la t0, bullet_x
    add t5, t0, t4
    lw a0, 0(t5)

    # Carrega bullet_y[indice] em a1
    la t0, bullet_y
    add t5, t0, t4
    lw a1, 0(t5)

    # Protecao: x precisa estar entre 0 e 317
    blt a0, zero, next_draw_bullet
    li t6, BULLET_SIZE
    add t0, a0, t6
    li t6, SCREEN_WIDTH
    bgt t0, t6, next_draw_bullet

    # Protecao: y precisa estar entre 0 e 237
    blt a1, zero, next_draw_bullet
    li t6, BULLET_SIZE
    add t0, a1, t6
    li t6, SCREEN_HEIGHT
    bgt t0, t6, next_draw_bullet

    sw t1, 4(sp)
    sw a0, 8(sp)
    sw a1, 12(sp)

    la a2, sprite_projectile_pistol
    la t0, bullet_damage
    add t5, t0, t4
    lw t6, 0(t5)
    li t5, WEAPON_SHOTGUN_DAMAGE
    beq t6, t5, select_shotgun_projectile
    li t5, WEAPON_BOSS_DAMAGE
    beq t6, t5, select_uzi_projectile
    j draw_selected_player_projectile

select_shotgun_projectile:
    la a2, sprite_projectile_shotgun
    j draw_selected_player_projectile

select_uzi_projectile:
    la a2, sprite_projectile_uzi

draw_selected_player_projectile:
    lw a0, 8(sp)
    lw a1, 12(sp)
    li a3, 3
    li a4, 3
    call draw_sprite_8bpp_fast
    lw t1, 4(sp)

next_draw_bullet:
    addi t1, t1, 1
    j draw_bullets_loop

end_draw_bullets:
    lw ra, 0(sp)
    addi sp, sp, 16

    ret

# ------------------------------------------------------------
# draw_enemies
# Desenha inimigos ativos como sprites 16x16 ou fallback simples.
# ------------------------------------------------------------

draw_enemies:
    addi sp, sp, -24
    sw ra, 0(sp)

    li t0, USE_SPRITE_ENEMIES
    bnez t0, draw_enemies_skip_base

    call get_draw_base_address
    sw a0, 4(sp)
    j draw_enemies_begin_loop

draw_enemies_skip_base:
    sw zero, 4(sp)

draw_enemies_begin_loop:
    li t1, 0

draw_enemies_loop:
    li t3, MAX_ENEMIES
    beq t1, t3, end_draw_enemies

    slli t4, t1, 2

    la t0, enemy_active
    add t5, t0, t4
    lw t6, 0(t5)
    beqz t6, next_draw_enemy

    la t0, enemy_x
    add t5, t0, t4
    lw a0, 0(t5)

    la t0, enemy_y
    add t5, t0, t4
    lw a1, 0(t5)

    blt a0, zero, next_draw_enemy
    li t6, ENEMY_SIZE
    add t0, a0, t6
    li t6, SCREEN_WIDTH
    bgt t0, t6, next_draw_enemy

    blt a1, zero, next_draw_enemy
    li t6, ENEMY_SIZE
    add t0, a1, t6
    li t6, SCREEN_HEIGHT
    bgt t0, t6, next_draw_enemy

    sw t1, 8(sp)
    sw a0, 12(sp)
    sw a1, 16(sp)

    li t0, USE_SPRITE_ENEMIES
    beqz t0, draw_enemy_fallback_rect

    la t0, enemy_type
    add t5, t0, t4
    lw t6, 0(t5)

    la t0, animation_frame
    lw a5, 0(t0)
    andi a5, a5, 1

    li t5, RAT_ECHO
    beq t6, t5, select_enemy_echo_sprite

    li t5, RAT_MUTANT
    beq t6, t5, select_enemy_mutant_sprite

    li t5, RAT_SPITTER
    beq t6, t5, select_enemy_spitter_sprite

select_enemy_common_sprite:
    la a2, sprite_enemy_common_0
    beqz a5, draw_selected_enemy_sprite
    la a2, sprite_enemy_common_1
    j draw_selected_enemy_sprite

select_enemy_echo_sprite:
    la a2, sprite_enemy_echo_0
    beqz a5, draw_selected_enemy_sprite
    la a2, sprite_enemy_echo_1
    j draw_selected_enemy_sprite

select_enemy_mutant_sprite:
    la a2, sprite_enemy_mutant_0
    beqz a5, draw_selected_enemy_sprite
    la a2, sprite_enemy_mutant_1
    j draw_selected_enemy_sprite

select_enemy_spitter_sprite:
    # Somente o Spitter novo usa direcao visual; gameplay permanece inalterado.
    la t0, player_x
    lw t2, 0(t0)
    lw t3, 12(sp)
    sub t2, t2, t3
    mv t4, t2
    bgez t4, spitter_abs_dx_ready
    sub t4, zero, t4

spitter_abs_dx_ready:
    la t0, player_y
    lw t3, 0(t0)
    lw t5, 16(sp)
    sub t3, t3, t5
    mv a4, t3
    bgez a4, spitter_abs_dy_ready
    sub a4, zero, a4

spitter_abs_dy_ready:
    add t5, t4, a4
    sw t5, 20(sp)
    ble t4, a4, select_spitter_vertical_direction
    li a4, DIR_RIGHT
    bgez t2, spitter_direction_ready
    li a4, DIR_LEFT
    j spitter_direction_ready

select_spitter_vertical_direction:
    li a4, DIR_DOWN
    bgez t3, spitter_direction_ready
    li a4, DIR_UP

spitter_direction_ready:
    lw t5, 20(sp)
    li t0, SPITTER_PROJECTILE_RANGE
    bgt t5, t0, select_enemy_spitter_walk_sprite
    lw t4, 8(sp)
    slli t4, t4, 2
    la t0, enemy_attack_timer
    add t0, t0, t4
    lw t5, 0(t0)
    bnez t5, select_enemy_spitter_walk_sprite
    la a2, sprite_enemy_spitter_attack_table
    j load_enemy_sprite_from_table

select_enemy_spitter_walk_sprite:
    la a2, sprite_enemy_spitter_walk_1_table
    beqz a5, load_enemy_sprite_from_table
    la a2, sprite_enemy_spitter_walk_2_table

load_enemy_sprite_from_table:
    slli a4, a4, 2
    add a2, a2, a4
    lw a2, 0(a2)

draw_selected_enemy_sprite:
    lw a0, 12(sp)
    lw a1, 16(sp)
    li a3, ENEMY_SIZE
    li a4, ENEMY_SIZE
    call draw_sprite_8bpp_fast

    lw t1, 8(sp)
    j next_draw_enemy

draw_enemy_fallback_rect:
    lw a0, 12(sp)
    lw a1, 16(sp)
    li a2, ENEMY_SIZE
    li a3, ENEMY_SIZE
    li a4, COLOR_ENEMY_FALLBACK
    lw a5, 4(sp)
    call draw_rect

    lw t1, 8(sp)

next_draw_enemy:
    addi t1, t1, 1
    j draw_enemies_loop

end_draw_enemies:
    lw ra, 0(sp)
    addi sp, sp, 24
    
    ret

# ------------------------------------------------------------
# draw_enemy_bullets
# Desenha projeteis inimigos ativos como quadrados 3x3.
#
# Entrada:
#   enemy_bullet_x
#   enemy_bullet_y
#   enemy_bullet_active
#
# Saida:
#   pixels escritos no frame atual
#
# Modifica: t0-t6, a0-a4
# ------------------------------------------------------------

draw_enemy_bullets:
    addi sp, sp, -16
    sw ra, 0(sp)

    li t1, 0

draw_enemy_bullets_loop:
    li t3, MAX_ENEMY_BULLETS
    beq t1, t3, end_draw_enemy_bullets

    slli t4, t1, 2

    # verifica se ativo
    la t0, enemy_bullet_active
    add t5, t0, t4
    lw t6, 0(t5)
    beqz t6, next_draw_enemy_bullet

    # a0 = x
    la t0, enemy_bullet_x
    add t5, t0, t4
    lw a0, 0(t5)

    # a1 = y
    la t0, enemy_bullet_y
    add t5, t0, t4
    lw a1, 0(t5)

    # segurança x
    blt a0, zero, next_draw_enemy_bullet

    # segurança y
    blt a1, zero, next_draw_enemy_bullet

    la t0, enemy_bullet_size
    add t5, t0, t4
    lw t6, 0(t5)
    bgtz t6, enemy_draw_size_ok
    li t6, ENEMY_BULLET_SIZE

enemy_draw_size_ok:
    mv a5, t6

    add t0, a0, a5
    li t6, SCREEN_WIDTH
    bgt t0, t6, next_draw_enemy_bullet

    add t0, a1, a5
    li t6, SCREEN_HEIGHT
    bgt t0, t6, next_draw_enemy_bullet

    sw t1, 4(sp)
    sw a0, 8(sp)
    sw a1, 12(sp)

    la t0, enemy_bullet_type
    add t5, t0, t4
    lw t6, 0(t5)
    li t5, ENEMY_PROJECTILE_BOSS_HEAVY
    beq t6, t5, select_boss_projectile

select_spitter_projectile:
    la a2, sprite_projectile_spitter
    li a3, 4
    li a4, 4
    j draw_selected_enemy_projectile

select_boss_projectile:
    la a2, sprite_projectile_boss
    li a3, 6
    li a4, 6

draw_selected_enemy_projectile:
    lw a0, 8(sp)
    lw a1, 12(sp)
    li t5, ENEMY_PROJECTILE_BOSS_HEAVY
    bne t6, t5, enemy_projectile_position_ready
    addi a0, a0, 2
    addi a1, a1, 2

enemy_projectile_position_ready:
    call draw_sprite_8bpp_fast
    lw t1, 4(sp)

next_draw_enemy_bullet:
    addi t1, t1, 1
    j draw_enemy_bullets_loop

end_draw_enemy_bullets:
    lw ra, 0(sp)
    addi sp, sp, 16

    ret

# ------------------------------------------------------------
# draw_boss_square
# Desenha o boss ativo como sprite 32x32 ou fallback simples.
#
# Entrada:
#   boss_active
#   boss_x
#   boss_y
#
# Saida:
#   pixels escritos no frame atual
#
# Modifica: t0-t6, a0-a4
# ------------------------------------------------------------

draw_boss_square:
    addi sp, sp, -12
    sw ra, 0(sp)

    la t0, boss_active
    lw t1, 0(t0)
    beqz t1, end_draw_boss_square

    la t0, boss_x
    lw a0, 0(t0)

    la t0, boss_y
    lw a1, 0(t0)

    sw a0, 4(sp)
    sw a1, 8(sp)

    li t0, USE_SPRITE_BOSS
    beqz t0, draw_boss_fallback_rect

    la t0, animation_frame
    lw t1, 0(t0)
    andi t1, t1, 1

    la a2, sprite_boss_0
    beqz t1, draw_selected_boss_sprite
    la a2, sprite_boss_1

draw_selected_boss_sprite:
    lw a0, 4(sp)
    lw a1, 8(sp)
    li a3, BOSS_SIZE
    li a4, BOSS_SIZE
    call draw_sprite_8bpp_fast
    j end_draw_boss_square

draw_boss_fallback_rect:
    call get_draw_base_address
    mv a5, a0

    lw a0, 4(sp)
    lw a1, 8(sp)
    li a2, BOSS_SIZE
    li a3, BOSS_SIZE
    li a4, COLOR_BOSS_FALLBACK
    call draw_rect

end_draw_boss_square:
    lw ra, 0(sp)
    addi sp, sp, 12

    ret

