    li a1, 68
    li a2, 64
    li a3, 10
    li a4, COLOR_TOWN_DETAIL
    lw a5, 4(sp)
    call draw_rect

    li a0, 184
    li a1, 152
    li a2, 64
    li a3, 10
    li a4, COLOR_TOWN_DETAIL
    lw a5, 4(sp)
    call draw_rect
    j end_draw_background

draw_sewer_background:
    li a0, 96
    li a1, 44
    li a2, 12
    li a3, 76
    li a4, COLOR_SEWER_DETAIL
    lw a5, 4(sp)
    call draw_rect

    li a0, 212
    li a1, 118
    li a2, 12
    li a3, 76
    li a4, COLOR_SEWER_DETAIL
    lw a5, 4(sp)
    call draw_rect
    j end_draw_background

draw_laboratory_background:
    li a0, 144
    li a1, 88
    li a2, 40
    li a3, 36
    li a4, COLOR_LAB_PANEL
    lw a5, 4(sp)
    call draw_rect

    li a0, 40
    li a1, 58
    li a2, 72
    li a3, 10
    li a4, COLOR_LAB_DETAIL
    lw a5, 4(sp)
