# ------------------------------------------------------------
# init_video
# Inicializa explicitamente os dois framebuffers.
#
# Frame 0 começa visível.
# Frame 1 será o primeiro frame de desenho.
# ------------------------------------------------------------

init_video:
    # Exibe inicialmente o framebuffer 0
    li t0, VGAFRAMESELECT
    sw zero, 0(t0)

    # O primeiro desenho será feito no framebuffer 1
    la t0, draw_frame
    li t1, 1
    sw t1, 0(t0)

    # Limpa o framebuffer 0
    li a0, 0x00
    li a1, 0
    li a7, 148
    ecall

    # Limpa o framebuffer 1
    li a0, 0x00
    li a1, 1
    li a7, 148
    ecall

    ret

# ------------------------------------------------------------
# begin_frame
# Limpa o frame atual de desenho.
#
# Entrada: draw_frame
# Saida: frame atual limpo
# Modifica: t0, t1, a0, a1, a7
# ------------------------------------------------------------

begin_frame:
    la t0, draw_frame
    lw t1, 0(t0)

    li a0, 0x00
    mv a1, t1
    li a7, 148
    ecall

    ret

# ------------------------------------------------------------
# end_frame
# Mostra o frame recem desenhado e alterna draw_frame.
#
# Entrada: draw_frame
# Saida: frame exibido e draw_frame alternado
# Modifica: t0, t1
# ------------------------------------------------------------

end_frame:
    la t0, draw_frame
    lw t1, 0(t0)

    li t0, VGAFRAMESELECT
    sw t1, 0(t0)

    xori t1, t1, 1

    la t0, draw_frame
    sw t1, 0(t0)

    ret

# ------------------------------------------------------------
# get_draw_base_address
# Retorna em a0 o endereco base do frame atual de desenho.
#
# Entrada: draw_frame
# Saida: a0 = VGAADDRESSINI0 ou VGAADDRESSINI1
# Modifica: t0, t1
# ------------------------------------------------------------

get_draw_base_address:
    la t0, draw_frame
    lw t1, 0(t0)

    li a0, VGAADDRESSINI0
    beqz t1, end_get_draw_base_address

    li a0, VGAADDRESSINI1

end_get_draw_base_address:
    ret

