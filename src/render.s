# ------------------------------------------------------------
# draw_sprite_8bpp_fast
# Desenha sprite 8bpp no frame atual de desenho.
#
# Entrada:
#   a0 = x
#   a1 = y
#   a2 = endereco do sprite
#   a3 = largura
#   a4 = altura
#
# Regras:
#   pixel 0 e transparente
#   get_draw_base_address e chamado uma vez por sprite
#   caminho rapido sem bounds check por pixel quando cabe na tela
# ------------------------------------------------------------

draw_sprite_8bpp_fast:
    addi sp, sp, -28
    sw ra, 0(sp)
    sw a0, 4(sp)
    sw a1, 8(sp)
    sw a2, 12(sp)
    sw a3, 16(sp)
    sw a4, 20(sp)

    call get_draw_base_address
    sw a0, 24(sp)

    lw t0, 4(sp)
    lw t1, 8(sp)
    lw t2, 16(sp)
    lw t3, 20(sp)

    blt t0, zero, draw_sprite_safe_path
    blt t1, zero, draw_sprite_safe_path

    add t4, t0, t2
    li t5, SCREEN_WIDTH
    bgt t4, t5, draw_sprite_safe_path

    add t4, t1, t3
    li t5, SCREEN_HEIGHT
    bgt t4, t5, draw_sprite_safe_path

    lw a0, 24(sp)
    lw t2, 12(sp)
    li t4, 0

